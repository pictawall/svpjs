/**
 * @interface VideoAdapter
 */

/**
 * The DOM version of the player.
 *
 * @property
 * @type {!HTMLElement}
 * @name VideoAdapter#playerElement
 * @readonly
 */

/**
 * Returns the type of video handled by this player.
 *
 * @property
 * @type {!String}
 * @name VideoAdapter#kind
 * @readonly
 */

/**
 * The original video height (integer).
 *
 * @property
 * @type {!number}
 * @name VideoAdapter#videoHeight
 * @readonly
 */

/**
 * The original video width (integer).
 *
 * @property
 * @type {!number}
 * @name VideoAdapter#videoWidth
 * @readonly
 */

/**
 * The player height (integer).
 *
 * @property
 * @type {!number}
 * @name VideoAdapter#height
 */

/**
 * The player width (integer).
 *
 * @property
 * @type {!number}
 * @name VideoAdapter#width
 */

/**
 * The audio volume (double from 0.0 to 1.0).
 *
 * @property
 * @type {!number}
 * @name VideoAdapter#volume
 */

/**
 * Whether or not the controls are displayed.
 *
 * @property
 * @type {!boolean}
 * @name VideoAdapter#controls
 */

/**
 * The time in seconds (double) indicating the playback time.
 *
 * @property
 * @type {!number}
 * @name VideoAdapter#currentTime
 */

